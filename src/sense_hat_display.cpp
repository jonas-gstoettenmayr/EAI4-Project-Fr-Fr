SenseHatDisplay::SenseHatDisplay() {
  available_ = OpenFramebuffer();
}

SenseHatDisplay::~SenseHatDisplay() {
  Clear();
  if (framebuffer_ != nullptr && mapping_size_ > 0U) {
    munmap(framebuffer_, mapping_size_);
  }
  if (file_descriptor_ >= 0) {
    close(file_descriptor_);
  }
}

